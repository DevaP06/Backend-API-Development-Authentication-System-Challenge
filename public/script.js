// Tab Management
function showTab(tabName) {
    // Hide all tabs
    document.querySelectorAll('.tab-content').forEach(tab => {
        tab.classList.remove('active');
    });
    document.querySelectorAll('.tab-btn').forEach(btn => {
        btn.classList.remove('active');
    });
    
    // Show selected tab
    document.getElementById(tabName + '-tab').classList.add('active');
    event.target.classList.add('active');
}

// Authentication Functions
async function handleLogin(e) {
    e.preventDefault();
    
    const username = document.getElementById('login-username').value;
    const password = document.getElementById('login-password').value;
    
    addOutput('üîÑ AUTHENTICATING USER: ' + username, 'info');
    addOutput('üîó API ENDPOINT: ' + `${API_BASE}/login`, 'info');
    
    try {
        const response = await fetch(`${API_BASE}/login`, {
            method: 'POST',
            headers: {
                'Content-Type': 'application/json',
            },
            body: JSON.stringify({ username, password }),
            credentials: 'include'
        });
        
        addOutput('üì° RESPONSE STATUS: ' + response.status, 'info');
        
        // Check if response is JSON
        const contentType = response.headers.get('content-type');
        if (!contentType || !contentType.includes('application/json')) {
            const text = await response.text();
            addOutput('‚ùå INVALID RESPONSE FORMAT (Expected JSON, got HTML/Text)', 'error');
            addOutput('üìÑ RESPONSE: ' + text.substring(0, 200) + '...', 'error');
            return;
        }
        
        const data = await response.json();
        
        if (response.ok) {
            authToken = data.data.accessToken;
            currentUser = data.data.user;
            localStorage.setItem('authToken', authToken);
            
            addOutput('‚úÖ AUTHENTICATION SUCCESSFUL', 'success');
            addOutput(`üë§ WELCOME BACK, ${currentUser.username.toUpperCase()}`, 'success');
            addOutput('üîì ACCESS GRANTED TO DISCOVERY SYSTEM', 'success');
            
            updateAuthStatus();
            document.getElementById('login-form').reset();
            
            // Auto-switch to discovery tab
            setTimeout(() => {
                showTab('discovery');
                document.querySelector('[onclick="showTab(\'discovery\')"]').classList.add('active');
            }, 1000);
            
        } else {
            addOutput('‚ùå AUTHENTICATION FAILED: ' + data.message, 'error');
        }
    } catch (error) {
        addOutput('üö® CONNECTION ERROR: ' + error.message, 'error');
    }
}

